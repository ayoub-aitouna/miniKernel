#include "printf.h"

void	ft_put_unsigned(unsigned int num, int *i)
{
	char	c;

	if (num >= 10)
		ft_put_unsigned(num / 10, i);
	c = '0' + num % 10;
	putchar(c);
	(*i)++;
}
